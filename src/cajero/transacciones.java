    /**
     * Clase interna para representar un panel con una imagen de fondo.
     */
  class Fondo extends JPanel {

        private Image imagen;

        @Override
        public void paint(Graphics g) {
            imagen = new ImageIcon(getClass().getResource("/imagenes/fondo.png")).getImage();
            g.drawImage(imagen, 0, 0, getWidth(), getHeight(), this);
            setOpaque(false);
            super.paint(g);
        }
    }
    /**
     * Establece un √≠cono redimensionado en un bot√≥n.
     * 
     * @param url la ruta del √≠cono
     * @param b el bot√≥n en el que se establecer√° el √≠cono
     * @return el √≠cono redimensionado
     */
     public Icon setIcono(String url,JButton b){
         ImageIcon  icon= new ImageIcon(getClass().getResource(url)); 
         int ancho =b.getWidth();
         int alto =b.getHeight();
         ImageIcon icono= new ImageIcon(icon.getImage().getScaledInstance(ancho,alto,Image.SCALE_DEFAULT)); 
         return icono;
     }
    /**
     * Establece un √≠cono redimensionado en un bot√≥n cuando se presiona.
     * 
     * @param url la ruta del √≠cono
     * @param b el bot√≥n en el que se establecer√° el √≠cono
     * @param ancho el ancho del √≠cono redimensionado
     * @param alto el alto del √≠cono redimensionado
     * @return el √≠cono redimensionado
     */
     public Icon setIconoPrecionado(String url,JButton b,int ancho,int alto){
       
         ImageIcon icon= new ImageIcon(getClass().getResource(url));
          int  width = b.getWidth()- ancho;
          int height = b.getHeight() - alto;
          ImageIcon icono = new ImageIcon(icon.getImage().getScaledInstance(width,height,Image.SCALE_DEFAULT));
          return icono;
     }
